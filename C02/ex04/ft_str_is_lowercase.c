#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 'a' || str[i] > 'z')
		{
			return (0);
		}
		++i;
	}
	return (1);
}
