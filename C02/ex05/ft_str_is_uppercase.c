#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			return (0);
		}
		++i;
	}
	return (1);
}
