#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z'))
			++i;
		else
		{
			return (0);
			break ;
		}
	}
	return (1);
}
